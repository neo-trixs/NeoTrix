fn add(e: &mut KnowledgeEngine, t: &str, b: &str, tags: Vec<&str>, imp: f64) -> String {
    if e.entries.values().any(|x| x.title == t) {
        return String::new();
    }
    let entry = KnowledgeEntry::new(t, b, SourceType::KnowledgeBase, "kb:esoteric")
        .with_importance(imp)
        .with_tags(tags.into_iter().map(|s| s.to_string()).collect());
    e.add_entry(entry)
}

fn link_title(e: &mut KnowledgeEngine, from: &str, to: &str, rt: RelationType, w: f64, desc: &str) {
    let f = e
        .entries
        .values()
        .find(|x| x.title.contains(from))
        .map(|x| x.id.clone());
    let t = e
        .entries
        .values()
        .find(|x| x.title.contains(to))
        .map(|x| x.id.clone());
    if let (Some(fi), Some(ti)) = (f, t) {
        e.add_relation(&fi, &ti, rt, w, desc);
    }
}

