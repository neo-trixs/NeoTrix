fn add(e: &mut KnowledgeEngine, t: &str, b: &str, tags: Vec<&str>, imp: f64) -> String {
    if e.entries.values().any(|x| x.title.contains(t)) && t.len() > 4 { return String::new(); }
    let entry = KnowledgeEntry::new(t, b, SourceType::KnowledgeBase, "kb:methodology")
        .with_importance(imp).with_tags(tags.into_iter().map(|s| s.to_string()).collect());
    e.add_entry(entry)
}

