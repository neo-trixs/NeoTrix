fn add(engine: &mut KnowledgeEngine, title: &str, body: &str, source: &str, tags: Vec<&str>, imp: f64) -> String {
    let src = match source {
        "wiki" => SourceType::Wikipedia, "kb" => SourceType::KnowledgeBase,
        "arxiv" => SourceType::ArXiv, "book" => SourceType::Book, _ => SourceType::WebPage,
    };
    let entry = KnowledgeEntry::new(title, body, src, &format!("kb:{}", title))
        .with_importance(imp)
        .with_tags(tags.into_iter().map(|s| s.to_string()).collect());
    engine.add_entry(entry)
}

fn link(engine: &mut KnowledgeEngine, from: &str, to: &str, rt: RelationType, w: f64, desc: &str) {
    // Find entries by title prefix match
    let from_id = engine.entries.values().find(|e| e.title.contains(from)).map(|e| e.id.clone());
    let to_id = engine.entries.values().find(|e| e.title.contains(to)).map(|e| e.id.clone());
    if let (Some(f), Some(t)) = (from_id, to_id) {
        engine.add_relation(&f, &t, rt, w, desc);
    }
}

