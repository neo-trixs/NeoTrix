    println!("  ✅ 8 条叁界交叉关系");

    // ================================================================
    // 报告
    // ================================================================
    let after = engine.stats().total_entries;
    let new_count = after - before;

    if let Err(e) = engine.save() {
        eprintln!("❌ 保存失败: {}", e);
    } else {
        println!("\n💾 已保存到 {:?}", kb_path);
    }

    println!("\n╔══════════════════════════════════════════════════════════════╗");
    println!("║  🌐 真实世界知识引擎 — 最终报告                              ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  本次新增: {:>3} 条知识, {:>3} 条关系                      ║", new_count, engine.stats().total_relations);
    println!("║  累计条目: {:>3}                                          ║", after);
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  壹 · 天界 (宇宙)    {:>3} 条                               ║", heaven.len());
    println!("║  贰 · 地界 (地球)    {:>3} 条                               ║", earth.len());
    println!("║  叁 · 人界 (文明)    {:>3} 条                               ║", humanity.len());
    println!("╠══════════════════════════════════════════════════════════════╣");

    let all_dims = [("天界", "壹·天"), ("地界", "贰·地"), ("人界", "叁·人")];
    for (tag, label) in &all_dims {
        let count = engine.search_by_tag(tag, 100).len();
        let bar = "█".repeat(count.min(30));
        let empty = "░".repeat(30 - count.min(30));
        println!("║  {} {:8} |{}{}| {:>2}            ║", label, "", bar, empty, count);
    }
    println!("╚══════════════════════════════════════════════════════════════╝");

    // 推演示例：跨维度知识溯源
    println!("\n🔮 天地人知识整合推演:");

    let demo_queries = [
        ("宇宙", "天界", "宇宙从何而来？"),
        ("地球生命", "地界", "生命如何在地球上生存？"),
        ("人类文明", "人界", "人类如何发展出文明？"),
        ("天地人", "叁界", "天地人如何相互作用？"),
    ];

    for (q, label, explanation) in &demo_queries {
        println!("\n  [{:8}] {}", label, explanation);
        let results = engine.search(q, 3);
        for (i, (entry, score)) in results.iter().enumerate() {
            println!("    {}. [{:.2}] {}", i+1, score, entry.title);
            if i == 0 {
                let body = entry.body.as_str();
                let end = body.len().min(100);
                println!("       {}", &body[..end]);
            }
        }
    }

    let graph = engine.export_graph();
    let graph_path = PathBuf::from(&home).join(".neotrix").join("knowledge_graph.json");
    std::fs::write(&graph_path, serde_json::to_string_pretty(&graph).expect("json serialization failed")).expect("failed to write knowledge graph");
    println!("\n📊 知识图谱已导出 ({:?}) — {} 节点, {} 边", graph_path,
        graph["nodes"].as_array().map(|a| a.len()).unwrap_or(0),
        graph["edges"].as_array().map(|a| a.len()).unwrap_or(0));
