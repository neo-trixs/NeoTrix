fn add(e: &mut KnowledgeEngine, t: &str, b: &str, tags: Vec<&str>, imp: f64) {
    if e.entries.values().any(|x| x.title.contains(t) && t.len() > 6) { return; }
    e.add_entry(KnowledgeEntry::new(t, b, SourceType::KnowledgeBase, "kb:civ")
        .with_importance(imp).with_tags(tags.iter().map(|s| s.to_string()).collect()));
}

fn link(e: &mut KnowledgeEngine, f: &str, t: &str, w: f64, d: &str) {
    let fi = e.entries.values().find(|x| x.title.contains(f)).map(|x| x.id.clone());
    let ti = e.entries.values().find(|x| x.title.contains(t)).map(|x| x.id.clone());
    if let (Some(ff), Some(tt)) = (fi, ti) { e.add_relation(&ff, &tt, RelationType::Related, w, d); }
}

