    #[test]
    fn test_queue_and_apply_roundtrip() {
        let mut c = EvolutionCoordinator::new();
        let mut r = HandlerRegistry::new();
        let names = ["alpha", "beta", "gamma"];
        for (i, name) in names.iter().enumerate() {
            let tier = if i == 0 {
                LoadTier::Hot
            } else if i == 1 {
                LoadTier::Warm
            } else {
                LoadTier::Cold
            };
            c.queue_mutation(Mutation::AddHandler {
                name: name.to_string(),
                tier,
            });
        }
        assert_eq!(c.pending_mutations.len(), 3);
        let n = c.mutation_apply(&mut r);
        assert_eq!(n, 3);
        assert_eq!(r.count(), 3);
        assert!(c.pending_mutations.is_empty());
    }

