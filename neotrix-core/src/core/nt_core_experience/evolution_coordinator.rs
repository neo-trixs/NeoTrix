    #[test]
    fn test_mutation_apply_multiple() {
        let mut c = EvolutionCoordinator::new();
        let mut r = HandlerRegistry::new();
        r.register("keep", LoadTier::Cold);
        c.queue_mutation(Mutation::ChangeTier {
            name: "keep".into(),
            tier: LoadTier::Hot,
        });
        c.queue_mutation(Mutation::AddHandler {
            name: "new_a".into(),
            tier: LoadTier::Warm,
        });
        c.queue_mutation(Mutation::AddHandler {
            name: "new_b".into(),
            tier: LoadTier::Warm,
        });
        c.queue_mutation(Mutation::RemoveHandler {
            name: "nonexistent".into(),
        });
        let n = c.mutation_apply(&mut r);
        assert_eq!(n, 3, "3/4 should succeed; nonexistent skip");
        assert_eq!(r.tier("keep"), LoadTier::Hot);
        assert!(r.contains("new_a"));
        assert!(r.contains("new_b"));
        assert!(!r.contains("nonexistent"));
    }

