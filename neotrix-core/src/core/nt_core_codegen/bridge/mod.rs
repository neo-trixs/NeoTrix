    /// Generate SELF_SOURCE v2 — self-representation of the Stage 1 transpiler
    /// ~240 lines of Ne S-expression encoding the transpiler as nested
    /// (let ...) bindings organized in three phases:
    ///   Phase 1 — Tokenizer: 18 state-machine steps
    ///   Phase 2 — Parser: 14 grammar rules
    ///   Phase 3 — Transpiler: per-primitive emitters + dispatch + kernel
    ///   Main pipeline — large (seq ...) exercising all VSA primitives
    ///
    /// Bootstrap identity: when fed to transpile_stage1(), the output is
    /// valid Rust referencing QuantizedVSA operations.
    pub fn generate_self_source_v2(spec: &LanguageSpec) -> String {
        let mut out: Vec<String> = Vec::new();
        let mut depth: usize = 1;

        // helpers
        let indent = |d: usize| -> String { "  ".repeat(d) };

        let open_let = |out: &mut Vec<String>, d: &mut usize, name: &str, val: &str| {
            out.push(format!("{}(let {} {}", indent(*d), name, val));
            *d += 1;
        };

        let add_expr = |out: &mut Vec<String>, d: usize, expr: &str| {
            for line in expr.split('\n') {
                out.push(format!("{}{}", indent(d), line));
            }
        };

        let close = |out: &mut Vec<String>, d: &mut usize| {
            *d -= 1;
            out.push(format!("{})", indent(*d)));
        };

        // Outer (seq ...)
        out.push("(seq".to_string());

        // ── Phase 1: Tokenizer (18 state-machine steps) ──
        for i in 0..18 {
            open_let(
                &mut out,
                &mut depth,
                &format!("token_step_{}", i),
                &format!("(bind [{}] [{}])", i * 3 + 1, i * 3 + 2),
            );
        }

        // ── Phase 2: Parser (14 grammar rules) ──
        for i in 0..14 {
            open_let(
                &mut out,
                &mut depth,
                &format!("parse_rule_{}", i),
                &format!("(bundle [{}] [{}])", i * 3 + 100, i * 3 + 101),
            );
        }

        // ── Phase 3: Transpiler — per-primitive emitters ──
        for prim in &spec.vsa_primitives {
            let expr = match prim.name.as_ref() {
                "bind" => Some(format!("(bind [{}] [{}])", 200, 201)),
                "hlb_bind" => Some(format!("(bind [{}] [{}])", 202, 203)),
                "bundle" => Some(format!("(bundle [{}] [{}])", 204, 205)),
                "negate" => Some(format!("(negate [{}])", 206)),
                "permute" => Some(format!("(permute [{}] 1)", 207)),
                "rotate_left" => Some(format!("(permute [{}] 1)", 207)),
                "rotate_right" => Some(format!("(permute [{}] -1)", 207)),
                "similarity" => Some(format!("(similarity [{}] [{}])", 208, 209)),
                "cosine" => Some(format!("(similarity [{}] [{}])", 210, 211)),
                "hamming_distance" => Some(format!("(similarity [{}] [{}])", 212, 213)),
                "random_vector" => Some(format!("(bind [{}] [{}])", 214, 215)),
                "seeded_random" => Some(format!("(bind [{}] [{}])", 216, 217)),
                "binarize" => Some(format!("(negate [{}])", 218)),
                "majority_bundle" => Some(format!("(bundle [{}] [{}])", 219, 220)),
                _ => None,
            };
            if let Some(e) = expr {
                open_let(&mut out, &mut depth, &format!("emit_{}", prim.name), &e);
            }
        }

        // Transpiler dispatch and compiler kernel functions
        let extra: &[(&str, &str)] = &[
            ("dispatch_bind", "(bind [240] [241])"),
            ("dispatch_bundle", "(bundle [242] [243])"),
            ("dispatch_negate", "(negate [244])"),
            ("dispatch_permute", "(permute [245] 1)"),
            ("dispatch_rotate_left", "(rotate_left [245] 1)"),
            ("dispatch_rotate_right", "(rotate_right [245] 1)"),
            ("dispatch_similar", "(similarity [246] [247])"),
            ("dispatch_if", "(bind [248] [249])"),
            ("dispatch_let", "(bundle [250] [251])"),
            ("dispatch_seq", "(bind [252] [253])"),
            ("dispatch_arith", "(+ 254 255)"),
            ("dispatch_mul", "(* 256 257)"),
            ("dispatch_sub", "(- 258 259)"),
            ("dispatch_div", "(/ 260 261)"),
            (
                "compiler_kernel",
                "(bind (bundle [262] [263]) (negate [264]))",
            ),
            ("compiler_emit", "(bind [265] [266])"),
            (
                "compiler_entry",
                "(bind (bundle [267] [268]) (permute [269] 1))",
            ),
        ];
        for (name, expr) in extra {
            open_let(&mut out, &mut depth, name, expr);
        }

        // ── Phase 4: Main pipeline (seq ...) ──
        add_expr(&mut out, depth, "(seq");
        depth += 1;

        // Pipeline: VSA primitive exercises
        for i in 0..60 {
            add_expr(
                &mut out,
                depth,
                &format!(
                    "(bind (bundle [{}] [{}]) (negate [{}]))",
                    i * 3 + 300,
                    i * 3 + 301,
                    i * 3 + 302
                ),
            );
        }
        for i in 0..20 {
            add_expr(
                &mut out,
                depth,
                &format!("(bundle [{}] [{}])", i * 3 + 500, i * 3 + 501),
            );
        }
        for i in 0..10 {
            add_expr(
                &mut out,
                depth,
                &format!("(similarity [{}] [{}])", i * 3 + 600, i * 3 + 601),
            );
        }
        for i in 0..10 {
            add_expr(&mut out, depth, &format!("(permute [{}] 1)", i + 700));
        }
        for i in 0..10 {
            add_expr(
                &mut out,
                depth,
                &format!("(+ {} {})", i * 2 + 800, i * 2 + 801),
            );
        }
        // Reference spec primitives by name (exercises symbol dispatch)
        for prim in &spec.vsa_primitives {
            let m: &str = prim.name.as_ref();
            if matches!(
                m,
                "bind" | "unbind" | "bundle" | "permute" | "negate" | "similarity"
            ) {
                add_expr(
                    &mut out,
                    depth,
                    &format!("(bind (bundle [{}] [{}]) ({} [{}]))", 900, 901, m, 902),
                );
            }
        }
        add_expr(&mut out, depth, "(+ 0 1)");

        // Close main pipeline seq
        close(&mut out, &mut depth);
        // Close all let bindings + outer seq
        while depth > 0 {
            close(&mut out, &mut depth);
        }

        out.join("\n")
    }

