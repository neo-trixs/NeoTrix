    /// Transpile Ne Stage 1 S-expressions to Rust code.
    /// Uses a built-in S-expression parser to avoid cross-crate dependency issues
    /// with the nt_core_language parser.
    ///
    /// Input: `(bind [1 2 3] [4 5 6])`
    /// Output: A complete Rust fn using QuantizedVSA operations.
    pub fn transpile_stage1(ne_source: &str) -> Result<String, String> {
        let tokens = tokenize(ne_source);
        let mut pos = 0;
        let expr = parse_sexpr(&tokens, &mut pos)
            .map_err(|e| format!("Parse error at position {}: {}", pos, e))?;
        if pos < tokens.len() {
            return Err(format!(
                "Unexpected extra tokens after position {}: {:?}",
                pos,
                &tokens[pos..]
            ));
        }
        let rust_expr = transpile_sexpr(&expr);
        Ok(format!(
            r#"use crate::core::nt_core_hcube::vsa_quantized::QuantizedVSA;

pub fn stage1_main() -> String {{
    let result = {{ {} }};
    format!("{{:?}}", result)
}}"#,
            rust_expr
        ))
    }

