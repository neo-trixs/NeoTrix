    #[test]
    fn test_nt_suite_yaml_roundtrip() {
        let suite = NtSuite {
            name: "my_module".into(),
            description: "Tests for my_module".into(),
            tests: vec![
                NtTest {
                    name: "test_a".into(),
                    description: "Test A".into(),
                    imports: Some(vec!["std::collections::HashMap".into()]),
                    setup: Some("let map = HashMap::new();".into()),
                    code: "assert!(map.is_empty());".into(),
                },
            ],
        };
        let yaml = serde_yaml::to_string(&suite).unwrap();
        let deserialized: NtSuite = serde_yaml::from_str(&yaml).unwrap();
        assert_eq!(deserialized.name, "my_module");
        assert_eq!(deserialized.tests.len(), 1);
        assert_eq!(deserialized.tests[0].name, "test_a");
        assert_eq!(deserialized.tests[0].imports, Some(vec!["std::collections::HashMap".into()]));
    }

    #[test]
    fn test_nt_test_skip_optional_fields() {
        let test = NtTest {
            name: "test_b".into(),
            description: "Test B".into(),
            imports: None,
            setup: None,
            code: "assert!(true);".into(),
        };
        let yaml = serde_yaml::to_string(&test).unwrap();
        assert!(!yaml.contains("imports"));
        assert!(!yaml.contains("setup"));
        let deserialized: NtTest = serde_yaml::from_str(&yaml).unwrap();
        assert_eq!(deserialized.name, "test_b");
        assert!(deserialized.imports.is_none());
    }
