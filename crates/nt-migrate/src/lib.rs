#[derive(Debug)]
pub struct TestModule {
    pub file: PathBuf,
    pub mod_name: String,
    pub test_fns: Vec<String>,
}

#[derive(Debug, Serialize, Deserialize)]
pub struct NtSuite {
    pub name: String,
    pub description: String,
    pub tests: Vec<NtTest>,
}

#[derive(Debug, Serialize, Deserialize)]
pub struct NtTest {
    pub name: String,
    pub description: String,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub imports: Option<Vec<String>>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub setup: Option<String>,
    pub code: String,
}

/// Parse a Rust file, find a #[test] function by name, and return
/// (imports_from_parent_module, setup_code_before_assertions, code_body).
pub fn extract_test_code(file_path: &Path, fn_name: &str) -> Result<(Vec<String>, Option<String>, String)> {
    let content = fs::read_to_string(file_path)
        .with_context(|| format!("Failed to read {}", file_path.display()))?;
    let parsed: File = syn::parse_file(&content)
        .with_context(|| format!("Failed to parse {}", file_path.display()))?;

    for item in &parsed.items {
        if let Item::Mod(m) = item {
            if !has_cfg_test(&m.attrs) && m.ident != "tests" {
                continue;
            }
            let items = m.content.as_ref()
                .map(|(_, items)| items)
                .context(format!("Module '{}' has no body (out-of-line)", m.ident))?;

            let imports = extract_import_paths(items);

            let found = items.iter().any(|inner| {
                if let Item::Fn(f) = inner {
                    is_test_fn(&f.attrs) && f.sig.ident == fn_name
                } else {
                    false
                }
            });

            if found {
                let body = extract_function_body(&content, fn_name)?;
                let (setup, code) = split_body_text(&body);
                return Ok((imports, setup, code));
            }
        }
    }

    anyhow::bail!("Test function '{}' not found in {}", fn_name, file_path.display());
}

pub fn build_nt_suite(file: &Path, mod_name: &str) -> Result<NtSuite> {
    let content = fs::read_to_string(file)
        .with_context(|| format!("Failed to read {}", file.display()))?;
    let parsed: File = syn::parse_file(&content)
        .with_context(|| format!("Failed to parse {}", file.display()))?;

    for item in &parsed.items {
        if let Item::Mod(m) = item {
            if m.ident != mod_name {
                continue;
            }
            let items = m.content.as_ref()
                .map(|(_, items)| items)
                .context(format!("Module '{}' has no body (out-of-line)", mod_name))?;

            let module_imports = extract_import_paths(items);
            let suite_name = derive_suite_name(file, mod_name);
            let mut tests = Vec::new();

            for inner in items {
                if let Item::Fn(f) = inner {
                    if is_test_fn(&f.attrs) {
                        let fn_name = f.sig.ident.to_string();
                        let body = extract_function_body(&content, &fn_name)?;
                        let (setup, code) = split_body_text(&body);
                        tests.push(NtTest {
                            name: fn_name.clone(),
                            description: fn_name_to_description(&fn_name),
                            imports: Some(module_imports.clone()),
                            setup,
                            code,
                        });
                    }
                }
            }

            return Ok(NtSuite {
                name: suite_name,
                description: format!("{} — auto-generated by nt-migrate", mod_name),
                tests,
            });
        }
    }

    anyhow::bail!("Test module '{}' not found in {}", mod_name, file.display());
}

pub fn extract_function_body(source: &str, fn_name: &str) -> Result<String> {
    let pattern = format!("fn {}(", fn_name);
    let fn_start = source.find(&pattern)
        .ok_or_else(|| anyhow::anyhow!("Function '{}' not found in source", fn_name))?;

    let after_fn = &source[fn_start..];
    let brace_open = after_fn.find('{')
        .ok_or_else(|| anyhow::anyhow!("No body for function '{}'", fn_name))?;
    let body_start = fn_start + brace_open + 1;

    let mut depth: i32 = 1;
    let mut body_end = body_start;
    for (i, c) in source[body_start..].char_indices() {
        match c {
            '{' => depth += 1,
            '}' => {
                depth -= 1;
                if depth == 0 {
                    body_end = body_start + i;
                    break;
                }
            }
            _ => {}
        }
    }

    if depth != 0 {
        anyhow::bail!("Unmatched braces in function '{}'", fn_name);
    }

    Ok(source[body_start..body_end].to_string())
}

pub fn clean_body_text(body: &str) -> String {
    let body = body.trim_start_matches('\n').trim_start_matches('\r');

    let lines: Vec<&str> = body.lines().collect();
    if lines.is_empty() {
        return String::new();
    }

    let min_indent = lines.iter()
        .filter(|l| !l.trim().is_empty())
        .map(|l| l.len() - l.trim_start().len())
        .min()
        .unwrap_or(0);

    if min_indent == 0 {
        return body.trim_end().to_string();
    }

    lines.iter()
        .map(|l| {
            if l.len() >= min_indent {
                &l[min_indent..]
            } else {
                l.trim()
            }
        })
        .collect::<Vec<&str>>()
        .join("\n")
        .trim_end()
        .to_string()
}

pub fn split_body_text(body: &str) -> (Option<String>, String) {
    let body = clean_body_text(body);
    let mut setup_lines: Vec<&str> = Vec::new();
    let mut code_lines: Vec<&str> = Vec::new();
    let mut found_assertion = false;

    for line in body.lines() {
        let trimmed = line.trim();
        if !found_assertion && !trimmed.is_empty() && trimmed.contains("assert") {
            found_assertion = true;
        }
        if found_assertion {
            code_lines.push(line);
        } else {
            setup_lines.push(line);
        }
    }

    if !found_assertion && !setup_lines.is_empty() {
        code_lines = setup_lines;
        setup_lines = Vec::new();
    }

    let setup = if setup_lines.is_empty() {
        None
    } else {
        Some(setup_lines.join("\n"))
    };

    (setup, code_lines.join("\n"))
}

pub fn extract_import_paths(items: &[syn::Item]) -> Vec<String> {
    items.iter()
        .filter_map(|item| {
            if let Item::Use(u) = item {
                let s = quote::quote!(#u).to_string();
                let s = s.trim().to_string();
                let path = s
                    .strip_prefix("use ")
                    .unwrap_or(&s)
                    .strip_suffix(';')
                    .unwrap_or(&s)
                    .trim()
                    .to_string();
                Some(normalize_import_path(&path))
            } else {
                None
            }
        })
        .collect()
}

pub fn normalize_import_path(path: &str) -> String {
    let parts: Vec<&str> = path.split("::").collect();
    let normalized: Vec<String> = parts.iter()
        .map(|s| {
            let s = s.trim();
            if s.starts_with('{') && s.ends_with('}') {
                let inner = &s[1..s.len()-1];
                let items: Vec<String> = inner.split(',')
                    .map(|i| i.trim().to_string())
                    .collect();
                format!("{{{}}}", items.join(", "))
            } else {
                s.to_string()
            }
        })
        .collect();
    normalized.join("::")
}

pub fn has_cfg_test(attrs: &[Attribute]) -> bool {
    attrs.iter().any(|attr| {
        if !attr.path().is_ident("cfg") {
            return false;
        }
        if let Meta::List(list) = &attr.meta {
            list.tokens.to_string().contains("test")
        } else {
            false
        }
    })
}

pub fn is_test_fn(attrs: &[Attribute]) -> bool {
    attrs.iter().any(|attr| {
        attr.path().segments.last()
            .map(|s| s.ident == "test")
            .unwrap_or(false)
    })
}

pub fn extract_test_fns_from_items(items: &[Item]) -> Vec<String> {
    let mut fns = Vec::new();
    for item in items {
        match item {
            Item::Fn(f) if is_test_fn(&f.attrs) => {
                fns.push(f.sig.ident.to_string());
            }
            Item::Mod(m) => {
                if let Some((_, nested)) = &m.content {
                    fns.extend(extract_test_fns_from_items(nested));
                }
            }
            _ => {}
        }
    }
    fns
}

pub fn fn_name_to_description(name: &str) -> String {
    let name = name.strip_prefix("test_").unwrap_or(name);
    let words: Vec<&str> = name.split('_').collect();
    if words.is_empty() {
        return String::new();
    }
    let mut result = String::new();
    for (i, w) in words.iter().enumerate() {
        if i > 0 {
            result.push(' ');
        }
        if w.len() <= 4 && w.chars().all(|c| c.is_ascii_uppercase()) {
            result.push_str(w);
        } else if i == 0 {
            let mut chars = w.chars();
            match chars.next() {
                None => {}
                Some(c) => {
                    result.push(c.to_ascii_uppercase());
                    result.push_str(chars.as_str());
                }
            }
        } else {
            result.push_str(w);
        }
    }
    result
}

pub fn derive_suite_name(file: &Path, mod_name: &str) -> String {
    if let Some(stem) = file.file_stem() {
        let stem = stem.to_string_lossy();
        stem.replace('-', "_")
    } else {
        mod_name.to_string()
    }
}

pub fn find_suites_dir() -> Result<PathBuf> {
    let candidates = [
        Path::new("neotrix-core/test_suites"),
        Path::new("../neotrix-core/test_suites"),
    ];
    for c in &candidates {
        if c.is_dir() {
            return Ok(c.to_path_buf());
        }
    }
    anyhow::bail!("Cannot find test_suites/ directory (looked: {:?}). Run from workspace root.", candidates);
}

pub fn get_pos_arg<'a>(args: &'a [String], idx: usize) -> Option<&'a Path> {
    args.get(idx).map(Path::new)
}

pub fn parse_key_value_args(args: &[String], key: &str) -> Result<(PathBuf, String)> {
    if args.len() < 3 || args[1] != key {
        anyhow::bail!("Usage: nt-migrate ... <file.rs> {} <mod_name>", key);
    }
    Ok((PathBuf::from(&args[0]), args[2].clone()))
}

pub fn parse_named_arg<'a>(args: &'a [String], key: &str) -> Option<&'a str> {
    args.iter()
        .position(|a| a == key)
        .and_then(|i| args.get(i + 1))
        .map(|s| s.as_str())
}

pub fn scan_for_test_modules(path: &Path) -> Result<Vec<TestModule>> {
    let mut modules = Vec::new();

    for entry in WalkDir::new(path).into_iter().filter_entry(|e| {
        !e.file_name().to_string_lossy().starts_with('.')
    }) {
        let entry = entry.with_context(|| "Error walking directory")?;
        if !entry.file_type().is_file() {
            continue;
        }
        let p = entry.path();
        if p.extension().map(|ext| ext == "rs").unwrap_or(false) {
            let content = match fs::read_to_string(p) {
                Ok(c) => c,
                Err(_) => continue,
            };
            let parsed: File = match syn::parse_file(&content) {
                Ok(f) => f,
                Err(_) => continue,
            };

            for item in &parsed.items {
                if let Item::Mod(m) = item {
                    if has_cfg_test(&m.attrs) || m.ident == "tests" {
                        let test_fns = extract_test_fns_from_items(
                            m.content.as_ref().map(|(_, items)| items).unwrap_or(&vec![])
                        );
                        if !test_fns.is_empty() {
                            modules.push(TestModule {
                                file: p.to_path_buf(),
                                mod_name: m.ident.to_string(),
                                test_fns,
                            });
                        }
                    }
                }
            }

            let free_fns: Vec<String> = parsed.items.iter()
                .filter_map(|item| {
                    if let Item::Fn(f) = item {
                        if is_test_fn(&f.attrs) {
                            Some(f.sig.ident.to_string())
                        } else {
                            None
                        }
                    } else {
                        None
                    }
                })
                .collect();

            if !free_fns.is_empty() {
                modules.push(TestModule {
                    file: p.to_path_buf(),
                    mod_name: "(root)".to_string(),
                    test_fns: free_fns,
                });
            }
        }
    }

    Ok(modules)
}

